package Class_12;

public class Helper {

    public void compareTwoInteger(int a, int b  ){

        if (a == b) {
            System.out.println(" a is equal to b");
        }
        else if (a<b){
            System.out.println("a is less than b");

        }
        else if (a>b){
            System.out.println("a is greater than b");
        }

        else {
            System.out.println(" I am inside else block");
        }

    }
}
